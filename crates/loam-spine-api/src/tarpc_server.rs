/// tarpc server for `LoamSpine`.
///
/// Implements the `LoamSpineRpc` trait generated by the tarpc macro.
#[derive(Clone)]
pub struct LoamSpineTarpcServer {
    service: Arc<LoamSpineRpcService>,
}

impl LoamSpineTarpcServer {
    /// Create a new tarpc server.
    #[must_use]
    pub fn new(service: LoamSpineRpcService) -> Self {
        Self {
            service: Arc::new(service),
        }
    }

    /// Create with default service.
    #[must_use]
    pub fn default_server() -> Self {
        Self::new(LoamSpineRpcService::default_service())
    }
}

